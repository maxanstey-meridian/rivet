    private static readonly HashSet<string> TsReservedWords = new()
    {
        "break", "case", "catch", "class", "const", "continue", "debugger",
        "default", "delete", "do", "else", "enum", "export", "extends",
        "false", "finally", "for", "function", "if", "import", "in",
        "instanceof", "new", "null", "return", "super", "switch", "this",
        "throw", "true", "try", "typeof", "var", "void", "while", "with",
        "yield", "let", "static", "implements", "interface", "package",
        "private", "protected", "public", "await", "async",
    };

    private static readonly Dictionary<string, string> ReservedWordReplacements = new()
    {
        ["delete"] = "remove",
    };

    /// <summary>
    /// Emits the shared rivet.ts module with rivetFetch + configureRivet.
    /// </summary>
    public static string EmitRivetBase()
    {
        var sb = new StringBuilder();
        sb.AppendLine("""
            // Generated by Rivet — do not edit

            export type RivetConfig = {
              baseUrl: string;
              headers?: () => Record<string, string> | Promise<Record<string, string>>;
              fetch?: typeof fetch;
              onError?: (error: RivetError) => never;
            };

            export type RivetResult<T> = { status: number; data: T; response: Response };

            export class RivetError extends Error {
              constructor(
                public readonly method: string,
                public readonly path: string,
                public readonly status: number,
                public readonly response: Response | undefined,
                public readonly body: unknown,
                options?: ErrorOptions,
              ) {
                super(`Rivet: ${method} ${path} returned ${status}`, options);
                this.name = "RivetError";
              }
            }

            let _config: RivetConfig = { baseUrl: "" };

            export const configureRivet = (config: RivetConfig): void => {
              _config = config;
            };

            const parseBody = async (res: Response): Promise<unknown> => {
              const text = await res.text().catch(() => "");
              if (!text) return undefined;
              const ct = res.headers.get("content-type") ?? "";
              if (ct.includes("application/json")) {
                try { return JSON.parse(text); } catch { return text; }
              }
              return text;
            };

            export const rivetFetch = async <T>(
              method: string,
              path: string,
              options?: { body?: unknown; query?: Record<string, unknown>; unwrap?: boolean },
            ): Promise<T> => {
              const url = new URL(`${_config.baseUrl}${path}`);
              if (options?.query) {
                for (const [k, v] of Object.entries(options.query)) {
                  if (v != null) url.searchParams.set(k, String(v));
                }
              }
              const headers: Record<string, string> = {
                ...(await _config.headers?.()),
              };
              const isFormData = typeof FormData !== "undefined" && options?.body instanceof FormData;
              if (options?.body && !isFormData) {
                headers["Content-Type"] = "application/json";
              }
              const f = _config.fetch ?? fetch;
              let res: Response;
              try {
                res = await f(url.toString(), {
                  method,
                  headers,
                  body: options?.body ? (isFormData ? options.body as BodyInit : JSON.stringify(options.body)) : undefined,
                });
              } catch (err) {
                const error = new RivetError(method, path, 0, undefined, undefined, { cause: err });
                if (_config.onError) _config.onError(error);
                throw error;
              }
              if (options?.unwrap === false) {
                const body = await parseBody(res);
                return { status: res.status, data: body, response: res } as T;
              }
              if (!res.ok) {
                const body = await parseBody(res);
                const error = new RivetError(method, path, res.status, res, body, {
                  cause: new Error(`HTTP ${res.status}`),
                });
                if (_config.onError) _config.onError(error);
                throw error;
              }
              if (res.status === 204) {
                return undefined as T;
              }
              return (await res.json()) as T;
            };
            """);

        return sb.ToString();
    }

    /// <summary>
    /// Emits a per-controller client file with typed fetch functions.
    /// </summary>
    public static string EmitControllerClient(
        string controllerName,
        IReadOnlyList<TsEndpointDefinition> endpoints,
        IReadOnlyDictionary<string, string> typeFileMap,
        bool validated = false)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// Generated by Rivet — do not edit");
        sb.AppendLine();

        // Import rivetFetch + types from shared module
        var needsRivetError = validated && endpoints.Any(e => e.ReturnType is not null && e.Responses.Any(r => r.DataType is not null));
        var rivetImports = needsRivetError
            ? "import { rivetFetch, RivetError, type RivetResult } from \"../rivet.js\";"
            : "import { rivetFetch, type RivetResult } from \"../rivet.js\";";
        sb.AppendLine(rivetImports);

        // Import validator assertions from build output
        if (validated)
        {
            var assertImports = endpoints
                .Where(e => e.ReturnType is not null)
                .Select(e => ValidatorEmitter.GetAssertName(e.ReturnType!))
                .Concat(endpoints.SelectMany(e => e.Responses)
                    .Where(r => r.DataType is not null)
                    .Select(r => ValidatorEmitter.GetAssertName(r.DataType!)))
                .Distinct()
                .Order()
                .ToList();

            if (assertImports.Count > 0)
            {
                sb.AppendLine($"import {{ {string.Join(", ", assertImports)} }} from \"../build/validators.js\";");
            }
        }

        // Import referenced types from their source files
        var referencedTypes = CollectReferencedTypeNames(endpoints);
        if (referencedTypes.Count > 0)
        {
            var importsByFile = referencedTypes
                .Where(t => typeFileMap.ContainsKey(t))
                .GroupBy(t => typeFileMap[t])
                .OrderBy(g => g.Key);

            foreach (var group in importsByFile)
            {
                var names = string.Join(", ", group.Order());
                sb.AppendLine($"import type {{ {names} }} from \"../types/{group.Key}.js\";");
            }
        }

        // Emit each endpoint function
        foreach (var endpoint in endpoints)
        {
            sb.AppendLine();
            EmitEndpoint(sb, endpoint, validated);
        }

        return sb.ToString();
    }

    /// <summary>
    /// Groups endpoints by controller name.
    /// </summary>
    public static Dictionary<string, List<TsEndpointDefinition>> GroupByController(
        IReadOnlyList<TsEndpointDefinition> endpoints)
    {
        var groups = new Dictionary<string, List<TsEndpointDefinition>>();

        foreach (var endpoint in endpoints)
        {
            if (!groups.TryGetValue(endpoint.ControllerName, out var list))
            {
                list = new List<TsEndpointDefinition>();
                groups[endpoint.ControllerName] = list;
            }

            list.Add(endpoint);
        }

        return groups;
    }

    private static void EmitEndpoint(StringBuilder sb, TsEndpointDefinition endpoint, bool validated)
    {
        var funcName = SafeFunctionName(endpoint.Name);

        var successType = endpoint.ReturnType is not null
            ? TypeEmitter.EmitTypeString(endpoint.ReturnType)
            : "void";

        var paramParts = new List<string>();
        foreach (var p in endpoint.Params)
        {
            paramParts.Add($"{p.Name}: {TypeEmitter.EmitTypeString(p.Type)}");
        }

        var paramsStr = string.Join(", ", paramParts);

        var route = InterpolateRoute(endpoint.RouteTemplate, endpoint.Params);

        var fileParams = endpoint.Params.Where(p => p.Source == ParamSource.File).ToList();
        var bodyParam = endpoint.Params.FirstOrDefault(p => p.Source == ParamSource.Body);
        var queryParams = endpoint.Params.Where(p => p.Source == ParamSource.Query).ToList();

        // Determine result type for unwrap: false
        var hasResultDU = endpoint.Responses.Count >= 2;
        var resultTypeName = hasResultDU
            ? $"{ToPascalCase(funcName)}Result"
            : $"RivetResult<{successType}>";

        // Emit result DU type for multi-response endpoints
        if (hasResultDU)
        {
            EmitResultType(sb, resultTypeName, endpoint.Responses);
        }

        // Overload signatures
        var optsPrefix = paramsStr.Length > 0 ? $"{paramsStr}, " : "";
        sb.AppendLine($"export function {funcName}({paramsStr}): Promise<{successType}>;");
        sb.AppendLine($"export function {funcName}({optsPrefix}opts: {{ unwrap: true }}): Promise<{successType}>;");
        sb.AppendLine($"export function {funcName}({optsPrefix}opts: {{ unwrap: false }}): Promise<{resultTypeName}>;");

        // Build fetch options
        var fetchOptionParts = new List<string>();
        if (fileParams.Count > 0)
        {
            fetchOptionParts.Add("body: fd");
        }
        else if (bodyParam is not null)
        {
            fetchOptionParts.Add($"body: {bodyParam.Name}");
        }
        if (queryParams.Count > 0)
        {
            var queryEntries = queryParams.Select(p => $"{p.Name}");
            fetchOptionParts.Add($"query: {{ {string.Join(", ", queryEntries)} }}");
        }

        var baseFetchStr = fetchOptionParts.Count > 0
            ? $", {{ {string.Join(", ", fetchOptionParts)} }}"
            : "";

        fetchOptionParts.Add("unwrap: opts?.unwrap");
        var fullFetchStr = $", {{ {string.Join(", ", fetchOptionParts)} }}";

        var implReturn = $"Promise<{successType} | {resultTypeName}>";
        var needsAsync = fileParams.Count > 0 || (validated && endpoint.ReturnType is not null);
        var asyncKeyword = needsAsync ? "async " : "";

        // Implementation signature
        sb.AppendLine($"export {asyncKeyword}function {funcName}({optsPrefix}opts?: {{ unwrap?: boolean }}): {implReturn} {{");

        // FormData construction for file uploads
        if (fileParams.Count > 0)
        {
            sb.AppendLine("  const fd = new FormData();");
            foreach (var fp in fileParams)
            {
                sb.AppendLine($"  fd.append(\"{fp.Name}\", {fp.Name});");
            }
        }

        if (validated && endpoint.ReturnType is not null)
        {
            var assertName = ValidatorEmitter.GetAssertName(endpoint.ReturnType);
            if (endpoint.Responses.Any(r => r.DataType is not null))
            {
                sb.AppendLine($"  if (opts?.unwrap === false) {{");
                sb.AppendLine($"    const result = await rivetFetch<{resultTypeName}>(\"{endpoint.HttpMethod}\", `{route}`{fullFetchStr});");
                var responses = endpoint.Responses.Where(r => r.DataType is not null).ToList();
                for (var i = 0; i < responses.Count; i++)
                {
                    var responseAssert = ValidatorEmitter.GetAssertName(responses[i].DataType!);
                    var prefix = i == 0 ? "    if" : "    else if";
                    sb.AppendLine($"{prefix} (result.status === {responses[i].StatusCode}) result.data = {responseAssert}(result.data);");
                }
                sb.AppendLine($"    else throw new RivetError(\"{endpoint.HttpMethod}\", `{route}`, result.status, result.response, result.data);");
                sb.AppendLine("    return result;");
                sb.AppendLine("  }");
            }
            else
            {
                sb.AppendLine($"  if (opts?.unwrap === false) {{");
                sb.AppendLine($"    const result = await rivetFetch<{resultTypeName}>(\"{endpoint.HttpMethod}\", `{route}`{fullFetchStr});");
                sb.AppendLine($"    result.data = {assertName}(result.data);");
                sb.AppendLine("    return result;");
                sb.AppendLine("  }");
            }
            sb.AppendLine($"  const data = await rivetFetch<{successType}>(\"{endpoint.HttpMethod}\", `{route}`{baseFetchStr});");
            sb.AppendLine($"  return {assertName}(data);");
        }
        else
        {
            sb.AppendLine($"  return rivetFetch<{resultTypeName}>(\"{endpoint.HttpMethod}\", `{route}`{fullFetchStr});");
        }

        sb.AppendLine("}");
    }

    private static void EmitResultType(
        StringBuilder sb,
        string typeName,
        IReadOnlyList<TsResponseType> responses)
    {
        sb.Append($"export type {typeName} =");
        foreach (var r in responses)
        {
            var dataType = r.DataType is not null ? TypeEmitter.EmitTypeString(r.DataType) : "void";
            sb.AppendLine();
            sb.Append($"  | {{ status: {r.StatusCode}; data: {dataType}; response: Response }}");
        }
        sb.AppendLine(";");
        sb.AppendLine();
    }

    private static string ToPascalCase(string name)
    {
        if (string.IsNullOrEmpty(name) || char.IsUpper(name[0]))
        {
            return name;
        }

        return char.ToUpperInvariant(name[0]) + name[1..];
    }

    private static string SafeFunctionName(string name)
    {
        if (ReservedWordReplacements.TryGetValue(name, out var replacement))
        {
            return replacement;
        }

        if (TsReservedWords.Contains(name))
        {
            return $"_{name}";
        }

        return name;
    }

    private static string InterpolateRoute(string template, IReadOnlyList<TsEndpointParam> allParams)
    {
        var routeParams = allParams.Where(p => p.Source == ParamSource.Route).ToList();

        return RouteParamRegex().Replace(template, match =>
        {
            var paramName = match.Groups[1].Value;
            var param = routeParams.FirstOrDefault(p =>
                string.Equals(p.Name, paramName, StringComparison.OrdinalIgnoreCase));
            return param is not null ? $"${{{param.Name}}}" : match.Value;
        });
    }

    private static HashSet<string> CollectReferencedTypeNames(IReadOnlyList<TsEndpointDefinition> endpoints)
    {
        var names = new HashSet<string>();

        foreach (var endpoint in endpoints)
        {
            foreach (var param in endpoint.Params)
            {
                TsType.CollectTypeRefs(param.Type, names);
            }

            if (endpoint.ReturnType is not null)
            {
                TsType.CollectTypeRefs(endpoint.ReturnType, names);
            }

            foreach (var response in endpoint.Responses)
            {
                if (response.DataType is not null)
                {
                    TsType.CollectTypeRefs(response.DataType, names);
                }
            }
        }

        return names;
    }

    [GeneratedRegex(@"\{(\w+)\}")]
    private static partial Regex RouteParamRegex();
}
