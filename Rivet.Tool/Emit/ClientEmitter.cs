using System.Text;
using System.Text.RegularExpressions;
using Rivet.Tool.Model;

namespace Rivet.Tool.Emit;

/// <summary>
/// Renders TsEndpointDefinitions to per-controller TypeScript client files.
/// </summary>
public static partial class ClientEmitter
{
    private static readonly Dictionary<string, string> ReservedWordReplacements = new()
    {
        ["delete"] = "remove",
    };

    private sealed record ClientEmitContext(IReadOnlyDictionary<string, TsTypeDefinition> DefinitionsByName);

    /// <summary>
    /// Emits the shared rivet.ts module with rivetFetch + configureRivet.
    /// </summary>
    public static string EmitRivetBase()
    {
        var assembly = typeof(ClientEmitter).Assembly;
        var resourceName = assembly.GetManifestResourceNames()
            .FirstOrDefault(static name => name.EndsWith("rivet.ts", StringComparison.Ordinal));
        using var stream = resourceName is not null
            ? assembly.GetManifestResourceStream(resourceName)
            : null;
        if (stream is null)
        {
            throw new InvalidOperationException("Embedded resource ending with 'rivet.ts' not found.");
        }
        using var reader = new StreamReader(stream);
        return reader.ReadToEnd();
    }

    /// <summary>
    /// Emits a per-controller client file with typed fetch functions.
    /// </summary>
    public static string EmitControllerClient(
        string controllerName,
        IReadOnlyList<TsEndpointDefinition> endpoints,
        IReadOnlyDictionary<string, string> typeFileMap,
        ValidateMode validateMode = ValidateMode.None,
        IReadOnlyDictionary<string, TsTypeDefinition>? definitionsByName = null)
    {
        var context = new ClientEmitContext(definitionsByName ?? new Dictionary<string, TsTypeDefinition>());

        var sb = new StringBuilder();
        sb.AppendLine("// Generated by Rivet — do not edit");
        sb.AppendLine();

        // Import shared runtime helpers and result types.
        var needsResultOf = endpoints.Any(e => e.Responses.Count >= 2);
        var rivetImports = needsResultOf
            ? "import { rivetBuildPath, rivetBuildUrl, rivetFetch, rivetFetchRaw, type RivetRawClientOptions, type RivetResult, type RivetResultOf } from \"../rivet.js\";"
            : "import { rivetBuildPath, rivetBuildUrl, rivetFetch, rivetFetchRaw, type RivetRawClientOptions, type RivetResult } from \"../rivet.js\";";
        sb.AppendLine(rivetImports);

        // Import validator assertions
        if (validateMode == ValidateMode.Zod)
        {
            var assertImports = endpoints
                .Where(e => e.ReturnType is not null)
                .Select(e => ZodValidatorEmitter.GetAssertName(e.ReturnType!))
                .Concat(endpoints.SelectMany(e => e.Responses)
                    .Where(r => r.DataType is not null)
                    .Select(r => ZodValidatorEmitter.GetAssertName(r.DataType!)))
                .Distinct()
                .Order()
                .ToList();

            if (assertImports.Count > 0)
            {
                sb.AppendLine($"import {{ {string.Join(", ", assertImports)} }} from \"../validators.js\";");
            }
        }

        // Import referenced types from their source files
        var referencedTypes = CollectReferencedTypeNames(endpoints);
        if (referencedTypes.Count > 0)
        {
            var importsByFile = referencedTypes
                .Where(t => typeFileMap.ContainsKey(t))
                .GroupBy(t => typeFileMap[t])
                .OrderBy(g => g.Key);

            foreach (var group in importsByFile)
            {
                var names = string.Join(", ", group.Order());
                sb.AppendLine($"import type {{ {names} }} from \"../types/{group.Key}.js\";");
            }
        }

        // Emit each endpoint function
        foreach (var endpoint in endpoints)
        {
            sb.AppendLine();
            EmitEndpoint(sb, endpoint, validateMode, context);
        }

        return sb.ToString();
    }

    /// <summary>
    /// Groups endpoints by controller name.
    /// </summary>
    public static Dictionary<string, List<TsEndpointDefinition>> GroupByController(
        IReadOnlyList<TsEndpointDefinition> endpoints)
    {
        var groups = new Dictionary<string, List<TsEndpointDefinition>>();

        foreach (var endpoint in endpoints)
        {
            if (!groups.TryGetValue(endpoint.ControllerName, out var list))
            {
                list = new List<TsEndpointDefinition>();
                groups[endpoint.ControllerName] = list;
            }

            list.Add(endpoint);
        }

        return groups;
    }

    private static void EmitEndpoint(
        StringBuilder sb,
        TsEndpointDefinition endpoint,
        ValidateMode validateMode,
        ClientEmitContext context)
    {
        var funcName = SafeFunctionName(endpoint.Name);

        var isFileEndpoint = endpoint.FileContentType is not null;
        var successType = isFileEndpoint
            ? "Blob"
            : endpoint.ReturnType is not null
                ? TypeEmitter.EmitTypeString(endpoint.ReturnType)
                : "void";

        var routeParams = endpoint.Params.Where(p => p.Source == ParamSource.Route).ToList();
        var fileParams = endpoint.Params.Where(p => p.Source == ParamSource.File).ToList();
        var formFieldParams = endpoint.Params.Where(p => p.Source == ParamSource.FormField).ToList();
        var bodyParam = endpoint.Params.FirstOrDefault(p => p.Source == ParamSource.Body);
        var queryParams = endpoint.Params.Where(p => p.Source == ParamSource.Query).ToList();
        var requestBodyType = bodyParam is not null
            ? TypeEmitter.EmitTypeString(bodyParam.Type)
            : fileParams.Count == 0 && endpoint.RequestType is not null
                ? TypeEmitter.EmitTypeString(endpoint.RequestType)
                : null;

        var inputType = BuildInputType(
            routeParams,
            queryParams,
            fileParams,
            formFieldParams,
            requestBodyType,
            endpoint.InputTypeName,
            context);
        var paramsStr = inputType is not null ? $"input: {inputType}" : "";
        var route = InterpolateRoute(
            endpoint.RouteTemplate,
            routeParams,
            param => AccessProperty("input.params", param.Name));

        // Determine result type for unwrap: false
        var hasResultDU = endpoint.Responses.Count >= 2;
        var resultTypeName = hasResultDU
            ? $"{Naming.ToPascalCase(funcName)}Result"
            : $"RivetResult<{successType}>";

        // Emit result DU type for multi-response endpoints
        if (hasResultDU)
        {
            EmitResultType(sb, resultTypeName, endpoint.Responses, isFileEndpoint);
        }

        // Overload signatures
        var optsPrefix = paramsStr.Length > 0 ? $"{paramsStr}, " : "";
        sb.AppendLine($"export function {funcName}({paramsStr}): Promise<{successType}>;");
        sb.AppendLine($"export function {funcName}({optsPrefix}opts: {{ unwrap: true }}): Promise<{successType}>;");
        sb.AppendLine($"export function {funcName}({optsPrefix}opts: {{ unwrap: false }}): Promise<{resultTypeName}>;");
        sb.AppendLine($"export function {funcName}({optsPrefix}opts: RivetRawClientOptions): Promise<Response>;");

        // Build fetch options
        var fetchOptionParts = new List<string>();
        if (fileParams.Count > 0)
        {
            fetchOptionParts.Add("body: fd");
        }
        else if (bodyParam is not null || endpoint.RequestType is not null)
        {
            if (endpoint.IsFormEncoded)
            {
                fetchOptionParts.Add($"body: new URLSearchParams({AccessProperty("input", "body")} as Record<string, string>)");
                fetchOptionParts.Add("formEncoded: true");
            }
            else
            {
                fetchOptionParts.Add($"body: {AccessProperty("input", "body")}");
            }
        }
        if (queryParams.Count > 0)
        {
            fetchOptionParts.Add($"query: {AccessProperty("input", "query")}");
        }

        if (isFileEndpoint)
        {
            fetchOptionParts.Add("blob: true");
        }

        var baseFetchStr = fetchOptionParts.Count > 0
            ? $", {{ {string.Join(", ", fetchOptionParts)} }}"
            : "";

        fetchOptionParts.Add("unwrap: opts?.unwrap");
        var fullFetchStr = $", {{ {string.Join(", ", fetchOptionParts)} }}";
        var rawFetchStr = BuildRawFetchOptions(
            fileParams,
            bodyParam,
            queryParams,
            endpoint.RequestType,
            endpoint.IsFormEncoded);

        var implReturn = $"Promise<{successType} | {resultTypeName} | Response>";
        var hasTypedErrorResponses = endpoint.Responses.Any(r => r.DataType is not null && r.StatusCode >= 400);
        var needsValidation = validateMode != ValidateMode.None && (endpoint.ReturnType is not null || hasTypedErrorResponses);
        var needsAsync = fileParams.Count > 0 || needsValidation;
        var asyncKeyword = needsAsync ? "async " : "";

        // Implementation signature
        sb.AppendLine($"export {asyncKeyword}function {funcName}({optsPrefix}opts?: {{ unwrap?: boolean; raw?: false }} | RivetRawClientOptions): {implReturn} {{");

        // FormData construction for file uploads
        if (fileParams.Count > 0)
        {
            sb.AppendLine("  const fd = new FormData();");
            foreach (var fp in fileParams)
            {
                sb.AppendLine($"  fd.append(\"{fp.Name}\", {AccessProperty("input.body", fp.Name)});");
            }
            foreach (var ff in formFieldParams)
            {
                if (ff.Type is TsType.Primitive p && p.Name == "string")
                {
                    sb.AppendLine($"  fd.append(\"{ff.Name}\", {AccessProperty("input.body", ff.Name)});");
                }
                else
                {
                    sb.AppendLine($"  fd.append(\"{ff.Name}\", JSON.stringify({AccessProperty("input.body", ff.Name)}));");
                }
            }
        }

        sb.AppendLine("  if (opts?.raw === true) {");
        sb.AppendLine($"    return rivetFetchRaw(\"{endpoint.HttpMethod}\", `{route}`{rawFetchStr});");
        sb.AppendLine("  }");

        if (needsValidation)
        {
            // unwrap: false branch — validate error responses (and success if present)
            if (hasTypedErrorResponses)
            {
                sb.AppendLine($"  if (opts?.unwrap === false) {{");
                sb.AppendLine($"    const result = await rivetFetch<{resultTypeName}>(\"{endpoint.HttpMethod}\", `{route}`{fullFetchStr});");
                var responses = endpoint.Responses.Where(r => r.DataType is not null).ToList();
                for (var i = 0; i < responses.Count; i++)
                {
                    var responseAssert = ZodValidatorEmitter.GetAssertName(responses[i].DataType!);
                    var prefix = i == 0 ? "    if" : "    else if";
                    sb.AppendLine($"{prefix} (result.status === {responses[i].StatusCode}) {{");
                    sb.AppendLine($"      result.data = {responseAssert}(result.data);");
                    sb.AppendLine("      return result;");
                    sb.AppendLine("    }");
                }
                if (endpoint.ReturnType is not null)
                {
                    var successAssert = ZodValidatorEmitter.GetAssertName(endpoint.ReturnType);
                    sb.AppendLine("    else {");
                    sb.AppendLine($"      result.data = {successAssert}(result.data);");
                    sb.AppendLine("      return result;");
                    sb.AppendLine("    }");
                }
                else
                {
                    sb.AppendLine("    return result;");
                }
                sb.AppendLine("  }");
            }
            else if (endpoint.ReturnType is not null)
            {
                var assertName = ZodValidatorEmitter.GetAssertName(endpoint.ReturnType);
                sb.AppendLine($"  if (opts?.unwrap === false) {{");
                sb.AppendLine($"    const result = await rivetFetch<{resultTypeName}>(\"{endpoint.HttpMethod}\", `{route}`{fullFetchStr});");
                sb.AppendLine($"    result.data = {assertName}(result.data);");
                sb.AppendLine("    return result;");
                sb.AppendLine("  }");
            }

            // Unwrapped return — validate success type if present, otherwise plain fetch
            if (endpoint.ReturnType is not null)
            {
                var assertName = ZodValidatorEmitter.GetAssertName(endpoint.ReturnType);
                sb.AppendLine($"  const data = await rivetFetch<{successType}>(\"{endpoint.HttpMethod}\", `{route}`{baseFetchStr});");
                sb.AppendLine($"  return {assertName}(data);");
            }
            else
            {
                sb.AppendLine($"  return rivetFetch<{resultTypeName}>(\"{endpoint.HttpMethod}\", `{route}`{fullFetchStr});");
            }
        }
        else
        {
            sb.AppendLine($"  return rivetFetch<{resultTypeName}>(\"{endpoint.HttpMethod}\", `{route}`{fullFetchStr});");
        }

        sb.AppendLine("}");

        EmitPathFunctions(sb, endpoint, context);
    }

    private static string BuildRawFetchOptions(
        IReadOnlyList<TsEndpointParam> fileParams,
        TsEndpointParam? bodyParam,
        IReadOnlyList<TsEndpointParam> queryParams,
        TsType? requestType,
        bool isFormEncoded)
    {
        var optionParts = new List<string>();

        if (fileParams.Count > 0)
        {
            optionParts.Add("body: fd");
        }
        else if (bodyParam is not null || requestType is not null)
        {
            if (isFormEncoded)
            {
                optionParts.Add($"body: new URLSearchParams({AccessProperty("input", "body")} as Record<string, string>)");
                optionParts.Add("headers: { \"Content-Type\": \"application/x-www-form-urlencoded\", ...opts.headers }");
            }
            else
            {
                optionParts.Add($"body: JSON.stringify({AccessProperty("input", "body")})");
                optionParts.Add("headers: { \"Content-Type\": \"application/json\", ...opts.headers }");
            }
        }

        if (!optionParts.Any(static part => part.StartsWith("headers:", StringComparison.Ordinal)))
        {
            optionParts.Add("headers: opts.headers");
        }

        if (queryParams.Count > 0)
        {
            optionParts.Add($"query: {AccessProperty("input", "query")}");
        }

        optionParts.Add("redirect: opts.redirect");
        optionParts.Add("credentials: opts.credentials");
        optionParts.Add("signal: opts.signal");

        return $", {{ {string.Join(", ", optionParts)} }}";
    }

    private static void EmitPathFunctions(StringBuilder sb, TsEndpointDefinition endpoint, ClientEmitContext context)
    {
        var funcName = SafeFunctionName(endpoint.Name);
        var pathFuncName = $"{funcName}Path";
        var urlFuncName = $"{funcName}Url";
        var routeParams = endpoint.Params.Where(p => p.Source == ParamSource.Route).ToList();
        var queryParams = endpoint.Params.Where(p => p.Source == ParamSource.Query).ToList();
        var route = InterpolateRoute(
            endpoint.RouteTemplate,
            routeParams,
            param => AccessProperty("input.params", param.Name));
        var inputType = BuildPathInputType(routeParams, queryParams, endpoint.QueryAuth?.ParameterName, endpoint.InputTypeName, context);
        var paramsStr = inputType is null ? "" : $"input: {inputType}";
        var queryArg = queryParams.Count > 0 || endpoint.QueryAuth is not null
            ? ", input.query"
            : "";

        sb.AppendLine();
        sb.AppendLine($"export function {pathFuncName}({paramsStr}): string {{");
        sb.AppendLine($"  return rivetBuildPath(`{route}`{queryArg});");
        sb.AppendLine("}");
        sb.AppendLine();
        sb.AppendLine($"export function {urlFuncName}({paramsStr}): string {{");
        sb.AppendLine($"  return rivetBuildUrl(`{route}`{queryArg});");
        sb.AppendLine("}");
    }

    private static void EmitResultType(
        StringBuilder sb,
        string typeName,
        IReadOnlyList<TsResponseType> responses,
        bool isFileEndpoint = false)
    {
        var statusCodes = string.Join(" | ", responses.Select(r => r.StatusCode));
        sb.AppendLine($"export type {typeName} = RivetResultOf<");
        foreach (var r in responses)
        {
            // File endpoints: success responses (2xx with no DataType) carry Blob, not void
            var dataType = r.DataType is not null
                ? TypeEmitter.EmitTypeString(r.DataType)
                : isFileEndpoint && r.StatusCode is >= 200 and < 300
                    ? "Blob"
                    : "void";
            sb.AppendLine($"  | {{ status: {r.StatusCode}; data: {dataType}; response: Response }}");
        }
        sb.Append($"  | {{ status: Exclude<number, {statusCodes}>; data: unknown; response: Response }}");
        sb.AppendLine();
        sb.AppendLine(">;");
        sb.AppendLine();
    }

    private static string SafeFunctionName(string name)
    {
        if (ReservedWordReplacements.TryGetValue(name, out var replacement))
        {
            return replacement;
        }

        if (TsReservedWords.All.Contains(name))
        {
            return $"_{name}";
        }

        return name;
    }

    private static string? BuildInputType(
        IReadOnlyList<TsEndpointParam> routeParams,
        IReadOnlyList<TsEndpointParam> queryParams,
        IReadOnlyList<TsEndpointParam> fileParams,
        IReadOnlyList<TsEndpointParam> formFieldParams,
        string? requestBodyType,
        string? inputTypeName,
        ClientEmitContext context)
    {
        var fields = new List<string>();

        if (routeParams.Count > 0)
        {
            fields.Add($"params: {BuildObjectType(routeParams, inputTypeName, context)}");
        }

        if (queryParams.Count > 0)
        {
            fields.Add($"query: {BuildObjectType(queryParams, inputTypeName, context)}");
        }

        if (fileParams.Count > 0 || formFieldParams.Count > 0)
        {
            fields.Add($"body: {BuildObjectType(fileParams.Concat(formFieldParams).ToList(), inputTypeName, context)}");
        }
        else if (requestBodyType is not null)
        {
            fields.Add($"body: {requestBodyType}");
        }

        return fields.Count > 0 ? $"{{ {string.Join("; ", fields)}; }}" : null;
    }

    private static string? BuildPathInputType(
        IReadOnlyList<TsEndpointParam> routeParams,
        IReadOnlyList<TsEndpointParam> queryParams,
        string? queryAuthParam,
        string? inputTypeName,
        ClientEmitContext context)
    {
        var fields = new List<string>();

        if (routeParams.Count > 0)
        {
            fields.Add($"params: {BuildObjectType(routeParams, inputTypeName, context)}");
        }

        var queryShape = queryAuthParam is null
            ? queryParams
            : queryParams
                .Concat(new[] { new TsEndpointParam(queryAuthParam, new TsType.Primitive("string"), ParamSource.Query) })
                .ToList();

        if (queryShape.Count > 0)
        {
            fields.Add($"query: {BuildObjectType(queryShape, inputTypeName, context)}");
        }

        return fields.Count > 0 ? $"{{ {string.Join("; ", fields)}; }}" : null;
    }

    // Optionality can come directly from endpoint params or from DTO definitions loaded from JSON contracts.
    private static string BuildObjectType(
        IReadOnlyList<TsEndpointParam> parameters,
        string? inputTypeName,
        ClientEmitContext context)
    {
        return $"{{ {string.Join("; ", parameters.Select(
            p => $"{TypeEmitter.QuoteIfNeeded(p.Name)}{(IsParamOptional(p, inputTypeName, context) ? "?" : "")}: {TypeEmitter.EmitTypeString(p.Type)}"))}; }}";
    }

    private static bool IsParamOptional(TsEndpointParam param, string? inputTypeName, ClientEmitContext context)
    {
        // Check if the param's own type is nullable
        if (param.Type is TsType.Nullable)
        {
            return true;
        }

        // JSON contract may have optional: true with non-nullable type
        // Check definition for DTO types (complex case)
        if (param.Type is TsType.TypeRef typeRef && context.DefinitionsByName.TryGetValue(typeRef.Name, out var td))
        {
            return td.Properties.FirstOrDefault(p => p.Name == param.Name)?.IsOptional ?? false;
        }

        // For JSON contract inputs that have optional: true on a non-nullable type
        if (inputTypeName is not null && context.DefinitionsByName.TryGetValue(inputTypeName, out var td2))
        {
            return td2.Properties.FirstOrDefault(p => p.Name == param.Name)?.IsOptional ?? false;
        }

        return false;
    }

    private static string InterpolateRoute(
        string template,
        IReadOnlyList<TsEndpointParam> routeParams,
        Func<TsEndpointParam, string> emitValue)
    {
        var result = RouteParamRegex().Replace(template, match =>
        {
            var paramName = match.Groups[1].Value;
            var param = routeParams.FirstOrDefault(p =>
                string.Equals(p.Name, paramName, StringComparison.OrdinalIgnoreCase));
            if (param is not null)
            {
                return $"${{encodeURIComponent(String({emitValue(param)}))}}";
            }

            throw new InvalidOperationException(
                $"Route placeholder '{{{paramName}}}' in '{template}' has no matching parameter in TInput. " +
                "Add a property with a matching name to the input type.");
        });

        return result;
    }

    private static string AccessProperty(string target, string propertyName)
    {
        var property = TypeEmitter.QuoteIfNeeded(propertyName);
        return property == propertyName
            ? $"{target}.{propertyName}"
            : $"{target}[{property}]";
    }

    private static HashSet<string> CollectReferencedTypeNames(IReadOnlyList<TsEndpointDefinition> endpoints)
    {
        var names = new HashSet<string>();

        foreach (var endpoint in endpoints)
        {
            foreach (var param in endpoint.Params)
            {
                TsType.CollectTypeRefs(param.Type, names);
            }

            if (endpoint.ReturnType is not null)
            {
                TsType.CollectTypeRefs(endpoint.ReturnType, names);
            }

            var bodyParam = endpoint.Params.FirstOrDefault(p => p.Source == ParamSource.Body);
            if (bodyParam is null && endpoint.RequestType is not null)
            {
                TsType.CollectTypeRefs(endpoint.RequestType, names);
            }

            foreach (var response in endpoint.Responses)
            {
                if (response.DataType is not null)
                {
                    TsType.CollectTypeRefs(response.DataType, names);
                }
            }
        }

        return names;
    }

    [GeneratedRegex(@"\{(\w+)\}")]
    private static partial Regex RouteParamRegex();
}
