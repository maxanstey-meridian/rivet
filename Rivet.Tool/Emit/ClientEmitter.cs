using System.Text;
using System.Text.RegularExpressions;
using Rivet.Tool.Model;

namespace Rivet.Tool.Emit;

/// <summary>
/// Renders TsEndpointDefinitions to a TypeScript client.ts file
/// with typed fetch functions.
/// </summary>
public static partial class ClientEmitter
{
    public static string Emit(
        IReadOnlyList<TsEndpointDefinition> endpoints,
        IReadOnlyList<TsTypeDefinition> types,
        bool validated = false)
    {
        var sb = new StringBuilder();
        sb.AppendLine("// Generated by Rivet — do not edit");
        sb.AppendLine();

        // Import validator assertions from build output
        if (validated)
        {
            var assertImports = endpoints
                .Where(e => e.ReturnType is not null)
                .Select(e => ValidatorEmitter.GetAssertName(e.ReturnType!))
                .Distinct()
                .Order()
                .ToList();

            if (assertImports.Count > 0)
            {
                sb.AppendLine($"import {{ {string.Join(", ", assertImports)} }} from \"./build/validators.js\";");
            }
        }

        // Import referenced types from types.ts
        var referencedTypes = CollectReferencedTypeNames(endpoints);
        if (referencedTypes.Count > 0)
        {
            sb.AppendLine($"import type {{ {string.Join(", ", referencedTypes.Order())} }} from \"./types.js\";");
            sb.AppendLine();
        }

        // Emit rivetFetch declaration
        sb.AppendLine("export type RivetConfig = {");
        sb.AppendLine("  baseUrl: string;");
        sb.AppendLine("  headers?: () => Record<string, string> | Promise<Record<string, string>>;");
        sb.AppendLine("  fetch?: typeof fetch;");
        sb.AppendLine("};");
        sb.AppendLine();
        sb.AppendLine("let _config: RivetConfig = { baseUrl: \"\" };");
        sb.AppendLine();
        sb.AppendLine("export const configureRivet = (config: RivetConfig): void => {");
        sb.AppendLine("  _config = config;");
        sb.AppendLine("};");
        sb.AppendLine();
        sb.AppendLine("const rivetFetch = async <T>(");
        sb.AppendLine("  method: string,");
        sb.AppendLine("  path: string,");
        sb.AppendLine("  options?: { body?: unknown; query?: Record<string, string> },");
        sb.AppendLine("): Promise<T> => {");
        sb.AppendLine("  const url = new URL(`${_config.baseUrl}${path}`);");
        sb.AppendLine("  if (options?.query) {");
        sb.AppendLine("    for (const [k, v] of Object.entries(options.query)) {");
        sb.AppendLine("      url.searchParams.set(k, v);");
        sb.AppendLine("    }");
        sb.AppendLine("  }");
        sb.AppendLine("  const headers: Record<string, string> = {");
        sb.AppendLine("    ...(await _config.headers?.()),");
        sb.AppendLine("  };");
        sb.AppendLine("  if (options?.body) {");
        sb.AppendLine("    headers[\"Content-Type\"] = \"application/json\";");
        sb.AppendLine("  }");
        sb.AppendLine("  const f = _config.fetch ?? fetch;");
        sb.AppendLine("  const res = await f(url.toString(), {");
        sb.AppendLine("    method,");
        sb.AppendLine("    headers,");
        sb.AppendLine("    body: options?.body ? JSON.stringify(options.body) : undefined,");
        sb.AppendLine("  });");
        sb.AppendLine("  if (!res.ok) {");
        sb.AppendLine("    throw new Error(`Rivet: ${method} ${path} returned ${res.status}`);");
        sb.AppendLine("  }");
        sb.AppendLine("  if (res.status === 204) {");
        sb.AppendLine("    return undefined as T;");
        sb.AppendLine("  }");
        sb.AppendLine("  return (await res.json()) as T;");
        sb.AppendLine("};");

        // Emit each endpoint function
        foreach (var endpoint in endpoints)
        {
            sb.AppendLine();
            EmitEndpoint(sb, endpoint, validated);
        }

        return sb.ToString();
    }

    private static void EmitEndpoint(StringBuilder sb, TsEndpointDefinition endpoint, bool validated)
    {
        var returnTypeStr = endpoint.ReturnType is not null
            ? TypeEmitter.EmitTypeString(endpoint.ReturnType)
            : "void";

        // Build parameter list
        var paramParts = new List<string>();
        foreach (var p in endpoint.Params)
        {
            paramParts.Add($"{p.Name}: {TypeEmitter.EmitTypeString(p.Type)}");
        }

        var paramsStr = string.Join(", ", paramParts);

        // Build route with interpolation
        var route = InterpolateRoute(endpoint.RouteTemplate, endpoint.Params);

        // Build rivetFetch options
        var bodyParam = endpoint.Params.FirstOrDefault(p => p.Source == ParamSource.Body);
        var queryParams = endpoint.Params.Where(p => p.Source == ParamSource.Query).ToList();

        var optionsParts = new List<string>();
        if (bodyParam is not null)
        {
            optionsParts.Add($"body: {bodyParam.Name}");
        }
        if (queryParams.Count > 0)
        {
            var queryEntries = queryParams.Select(p => $"{p.Name}: String({p.Name})");
            optionsParts.Add($"query: {{ {string.Join(", ", queryEntries)} }}");
        }

        var optionsStr = optionsParts.Count > 0
            ? $", {{ {string.Join(", ", optionsParts)} }}"
            : "";

        // Validated mode: async + assert wrapper
        if (validated && endpoint.ReturnType is not null)
        {
            var assertName = ValidatorEmitter.GetAssertName(endpoint.ReturnType);
            sb.AppendLine($"export const {endpoint.Name} = async ({paramsStr}): Promise<{returnTypeStr}> => {{");
            sb.AppendLine($"  const raw = await rivetFetch<{returnTypeStr}>(\"{endpoint.HttpMethod}\", `{route}`{optionsStr});");
            sb.AppendLine($"  return {assertName}(raw);");
            sb.AppendLine("};");
        }
        else if (endpoint.ReturnType is not null)
        {
            sb.AppendLine($"export const {endpoint.Name} = ({paramsStr}): Promise<{returnTypeStr}> =>");
            sb.AppendLine($"  rivetFetch<{returnTypeStr}>(\"{endpoint.HttpMethod}\", `{route}`{optionsStr});");
        }
        else
        {
            sb.AppendLine($"export const {endpoint.Name} = ({paramsStr}): Promise<void> =>");
            sb.AppendLine($"  rivetFetch<void>(\"{endpoint.HttpMethod}\", `{route}`{optionsStr});");
        }
    }

    private static string InterpolateRoute(string template, IReadOnlyList<TsEndpointParam> allParams)
    {
        var routeParams = allParams.Where(p => p.Source == ParamSource.Route).ToList();

        // Replace {paramName} with ${paramName}
        return RouteParamRegex().Replace(template, match =>
        {
            var paramName = match.Groups[1].Value;
            // Find matching route param (case-insensitive)
            var param = routeParams.FirstOrDefault(p =>
                string.Equals(p.Name, paramName, StringComparison.OrdinalIgnoreCase));
            return param is not null ? $"${{{param.Name}}}" : match.Value;
        });
    }

    private static HashSet<string> CollectReferencedTypeNames(IReadOnlyList<TsEndpointDefinition> endpoints)
    {
        var names = new HashSet<string>();

        foreach (var endpoint in endpoints)
        {
            foreach (var param in endpoint.Params)
            {
                CollectTypeRefs(param.Type, names);
            }

            if (endpoint.ReturnType is not null)
            {
                CollectTypeRefs(endpoint.ReturnType, names);
            }
        }

        return names;
    }

    private static void CollectTypeRefs(TsType type, HashSet<string> names)
    {
        switch (type)
        {
            case TsType.TypeRef r:
                names.Add(r.Name);
                break;
            case TsType.Nullable n:
                CollectTypeRefs(n.Inner, names);
                break;
            case TsType.Array a:
                CollectTypeRefs(a.Element, names);
                break;
            case TsType.Dictionary d:
                CollectTypeRefs(d.Value, names);
                break;
            case TsType.Generic g:
                names.Add(g.Name);
                foreach (var arg in g.TypeArguments)
                {
                    CollectTypeRefs(arg, names);
                }
                break;
        }
    }

    [GeneratedRegex(@"\{(\w+)\}")]
    private static partial Regex RouteParamRegex();
}
